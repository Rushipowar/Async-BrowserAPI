// Definition of XMLHttpRequest Object:-
// The XMLHttpRequest object is a built-in browser object that allows developers to interact with servers. 
// It provides an easy way to retrieve data from a URL without having to do a full page refresh. This asynchronous 
// data fetching is a cornerstone of AJAX (Asynchronous JavaScript and XML).


// Usage in AJAX:-
// AJAX uses the XMLHttpRequest object to send and receive data from a web server asynchronously. This means that data 
// can be fetched in the background without interfering with the display and behavior of the existing page.
// 1.Update a web page without reloading it
// 2.Request data from a server after the page has loaded
// 3.Receive data from a server after the page has loaded
// 4.Send data to a server in the background
// 5.Create a highly user-friendly web application